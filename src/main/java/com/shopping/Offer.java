package com.shopping;

public class Offer {
	private Integer id;
	private String uuid;
	private String offer_description;
	private String offer_type;
	private String offer_title;
	private String minimum_offer_amount;
	private String offer_state;
	private String offer_expiry;
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public String getUuid() {
		return uuid;
	}
	public void setUuid(String uuid) {
		this.uuid = uuid;
	}
	public String getOffer_description() {
		return offer_description;
	}
	public void setOffer_description(String offer_description) {
		this.offer_description = offer_description;
	}
	public String getOffer_type() {
		return offer_type;
	}
	public void setOffer_type(String offer_type) {
		this.offer_type = offer_type;
	}
	public String getOffer_title() {
		return offer_title;
	}
	public void setOffer_title(String offer_title) {
		this.offer_title = offer_title;
	}
	public String getMinimum_offer_amount() {
		return minimum_offer_amount;
	}
	public void setMinimum_offer_amount(String minimum_offer_amount) {
		this.minimum_offer_amount = minimum_offer_amount;
	}
	public String getOffer_state() {
		return offer_state;
	}
	public void setOffer_state(String offer_state) {
		this.offer_state = offer_state;
	}
	public String getOffer_expiry() {
		return offer_expiry;
	}
	public void setOffer_expiry(String offer_expiry) {
		this.offer_expiry = offer_expiry;
	}
	
}
